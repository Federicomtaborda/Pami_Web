<h1>¿Qué función cumple el Médico de Cabecera?</h1>

<h2>El médico de cabecera es el principal referente y el nexo para la articulación de todos los procesos de atención relacionados con la salud de los afiliados. Sus principales funciones son:</h2>

<p>El Instituto Nacional de Servicios Sociales para Jubilados y Pensionados brinda un servicio altamente personalizado y en tal sentido, recibe reclamos, quejas, denuncias y sugerencias de los beneficiarios en todas aquellas áreas que tengan contacto directo con los afiliados:</p>

<ul>
    <li>Brindar Atención Médica Programada en Consultorio a los beneficiarios del sistema.</li>
    <li>El Consultorio del Médico de Cabecera deberá estar adecuadamente habilitado por autoridad competente y tener el Titulo Profesional a la vista, además del comprobante de inscripción en la Superintendencia de Servicios de Salud.</li>
    <li><b>El Médico de Cabecera no podrá, bajo ningún concepto, percibir suma alguna por parte del beneficiario.</b></li>
    <li>Es esencial que la atención en consultorio esté organizada mediante la fijación de turnos solicitados telefónicamente con anticipación, sin que ello implique que dejen de atenderse los beneficiarios que requieran atención para ese día con motivos de urgencia (se deberá reservar el 10% de los turnos).</li>
    <li>Para aquellos afiliados que no puedan concurrir al consultorio por dificultades motrices, las visitas se programarán dentro de las 24-48 hs. a partir de la solicitud, de lunes a viernes en horario a acordar entre médico y paciente. La atención a domicilio debe realizarse en horarios que no se superpongan con los de atención en consultorio.</li>
    <li>Prescribir Medicamentos acorde a los diagnósticos realizados.</li>
    <li>Indicar y evaluar prácticas y estudios complementarios. En todos los casos de interconsultas y/o internaciones el Médico de Cabecera está obligado a informar a los especialistas acerca de los antecedentes que figuren en la HCU (Historia Clínica Universal) que obra en su poder y/o enviar copia de la misma.</li>
    <li>El Médico de Cabecera tiene la facultad de solicitar (sin delegar ni consultar) al Médico especialista los análisis de laboratorio de rutina y estudios de diagnóstico por imágenes simples.</li>
    <li>Derivar pacientes a otros niveles de complejidad.</li>
    <li>Desarrollar acciones de prevención, protección y promoción de la salud y de previsión y rehabilitación de la enfermedad de acuerdo con Programas establecidos por la propia UGP, la autoridad Sanitaria jurisdiccional ó en el marco de Programas propios del Instituto.</li>
    <li>Coordinar y seguir longitudinalmente al paciente en los diferentes niveles de atención.</li>
    <li>Conducir la actitud de los pacientes y familiares evitando todo conflicto emocional ocasionado por la patología, allanando los caminos dentro del modelo.</li>
    <li>Promover y supervisar el correcto y racional uso de los recursos.</li>
    <li>Confeccionar y resguardar la historia clínica única, que implica el conocimiento previo del paciente, realizando el examen físico respectivo, completando antecedentes, factores de riesgo, etc., constando todos los diagnósticos de certeza. En todos los casos deberá consignarse la firma y el sello del Médico de Cabecera.</li>
    <li>Realizar actualización permanente de su profesión en patologías prevalentes en la Tercera Edad.</li>
    <li>Cada médico de cabecera contará con un profesional reemplazante. El profesional que reemplace al Médico de Cabecera (en caso de ausencia por enfermedad, viajes u otros motivos) debe ser, preferentemente, otro médico que pertenezca al Sistema y tenga relación contractual con la misma UGP que el titular. En caso de no pertenecer al Sistema, debe ser autorizado por el Instituto o la sucursal que corresponda. Podrá atender en el consultorio del titular o en el propio, siempre que esté en la misma zona geográfica, y deberá cumplir todas las obligaciones y requisitos establecidos por contrato para el titular.</li>
    <li>El médico de cabecera podrá derivar al afiliado a las prestaciones que brinda el Instituto (enfermería, servicio social, geriatría, discapacidad, salud mental, odontología, etc).</li>
    <li>Los pacientes crónicos que requieran kinesioterapia, deberán solicitarla, a través del Médico de Cabecera, en la sucursal de su zona, ya que este servicio no corresponde a la UGP.</li>

</ul>


